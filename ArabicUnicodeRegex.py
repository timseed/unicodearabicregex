#!/usr/bin/python3
import re
name="لها ألمّ إبّان الأخذ بـ. بفرض الآخر ويكيبيديا، مع بعض, مدن أم وقرى الأول. الغالي اليابانية أي يتم, ما أملاً احداث عرض. ممثّلة التحالف و حين. غير قد الإحتفاظ بريطانيا ايطاليا،, قد الا قادة علاقة الحيلولة. لكل إذ أراضي انتهت, ما فسقط مليون عرض, بـ بحق سبتمبر الإكتفاء التغييرات. فقد الجوي اتّجة الأخذ من, و تعد الشرقي مقاطعة البرية."
arabic=re.compile('[\u0600-\u06ff]+',re.UNICODE)
words=arabic.findall(name)
print("%s"%( name))
for w in words:
    print("%s"%(w))
